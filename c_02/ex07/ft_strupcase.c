char	*ft_strupcase(char *str)
{
	int	idx;

	idx = 0;
	while (str[idx] != '\0')
	{
		if ((str[idx] >= 'a' && str[idx] <= 'z'))
		{
			str[idx] -= 'a' - 'A';
		}
		idx++;
	}
	return (str);
}
