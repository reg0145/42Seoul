int	ft_str_is_alpha(char *s)
{
	int	idx;

	idx = 0;
	while (s[idx] != '\0')
	{
		if ((s[idx] < 'a' || s[idx] > 'z') && (s[idx] < 'A' || s[idx] > 'Z'))
		{
			return (0);
		}
		idx++;
	}
	return (1);
}
