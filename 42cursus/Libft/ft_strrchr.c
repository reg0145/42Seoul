#include "libft.h"

char	*strrchr(const char *s, int c)
{
	size_t	len;

	len = ft_strlen(s);
	while (len-- >= 0 && s[len] != 0)
	{
		if (s[len] == (char)c)
			return ((char *)&s[len]);
	}
	return (0);
}
